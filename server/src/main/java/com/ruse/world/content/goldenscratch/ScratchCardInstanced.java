package com.ruse.world.content.goldenscratch;

public class ScratchCardInstanced {

	public int buttonId, id;

	public ScratchCardInstanced(int buttonId, int id) {
		this.buttonId = buttonId;
		this.id = id;
	}

	public int getButtonId() {
		return buttonId;
	}

	public int getId() {
		return id;
	}

}